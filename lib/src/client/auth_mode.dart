/// リクエストの認証モード
enum AuthMode {
  /// 認証必須：トークンがなければエラー相当（デフォルト）
  ///
  /// POSTリクエストのbodyに`i`（トークン）を自動注入する。
  required,

  /// 認証任意：トークンがあれば付与するが、なくてもリクエストを送信する
  ///
  /// 未認証でも利用可能だが、認証済みの場合は追加情報
  /// （例: `isFollowing`, `isFavorited`）を含むレスポンスを返すエンドポイント向け。
  optional,

  /// 認証なし：トークンを付与しない
  ///
  /// 認証不要かつトークン送信が不適切なエンドポイント向け。
  none,
}
